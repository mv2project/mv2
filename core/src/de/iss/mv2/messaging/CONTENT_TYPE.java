package de.iss.mv2.messaging;

/**
 * Defines the content type of {@link MessageField}.
 * @author Marcel Singer
 *
 */
public enum CONTENT_TYPE {

	/**
	 * Defines that a message field contains a string literal.
	 */
	STRING_LITERAL,
	/**
	 * Defines that a message field contains binary content.
	 */
	BINARY
	
}
